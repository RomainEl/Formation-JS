/*-----------------------------------------------
        INCREMENTATION ET DECREMENTATION    
------------------------------------------------*/


//  ############ Incrémentation ################ //

var nb1 =  1;
nb1 = nb1 + 1;

// --Affichage dans la console
console.log(nb1);

// -- Ecriture simplifiée
nb1++; // Raccourci de nb1 = nb1 + 1; par pas de 1

// -- Affichage dans la console 
console.log(nb1);


//  ############ Décrémentation ################ //

nb1 = nb1 - 1;

// -- Ecriture simplifiée
nb1--; // Raccourci de nb1 = nb1 - 1; par pas de 1

// -- Affichage dans la console 
console.log(nb1);

// ############ NB ################## //

nb1 = 0;
console.log("nb1++ = " + nb1++);
nb1 = 0;
console.log("++nb1 = " + ++nb1);